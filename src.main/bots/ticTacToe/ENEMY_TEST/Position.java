package bots.ticTacToe.ENEMY_TEST;

public class Position {
  int x;
  int y;

  public Position() {
  }

  public Position(final int x, final int y) {
      this.x = x;
      this.y = y;
  }

  public int getX() {
      return x;
  }

  public void setX(final int x) {
      this.x = x;
  }

  public int getY() {
      return y;
  }

  public void setY(final int y) {
      this.y = y;
  }

}
